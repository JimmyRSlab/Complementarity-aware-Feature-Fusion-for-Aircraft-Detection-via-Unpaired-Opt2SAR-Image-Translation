import torch.nn.functional as F
def extract_targets_edges(image_tensor: torch.Tensor, gray_weights: torch.Tensor = None) -> torch.Tensor:
    """
    在目标区域提取边缘的PyTorch实现
    参数:
        image_tensor: 输入图像 [b,c,h,w]
        mask_tensor: 目标掩码 [b,c,h,w]（二值模板）
        gray_weights: 灰度转换权重，默认自动计算
    返回:
        edge_mask: 目标区域边缘掩码 [b,c,h,w]
    """
    b, c, h, w = image_tensor.shape
    device = image_tensor.device

    if gray_weights is None:
        if c == 3:
            gray_weights = torch.tensor([0.299, 0.587, 0.114], device=device)
        else:
            gray_weights = torch.ones(c, device=device) / c

    gray_weights = gray_weights.view(1, c, 1, 1)
    gray_img = (image_tensor * gray_weights).sum(dim=1, keepdim=True)

    threshold = 0.2
    scales = [1, 2]
    kernel_config = {
        1: {"size": 3, "padding": 1},  # 3x3
        2: {"size": 5, "padding": 2},  # 5x5
        3: {"size": 7, "padding": 3}  # 7x7
    }
    sobel_v_weights = {
        3: [1, 2, 1],
        5: [1, 4, 6, 4, 1],
        7: [1, 4, 6, 8, 6, 4, 1]}

    sobel_x_list = []
    sobel_y_list = []
    grad_x_list = []
    grad_y_list = []
    # grad_mag_list = []
    edge_mask_list = []

    for scale in scales:
        cfg = kernel_config[scale]
        ksize = cfg["size"]

        # 生成水平梯度向量（如[1,0,-1]）
        n = (ksize - 1) // 2
        h_grad = list(range(1, n + 1)) + [0] + list(range(-n, 0))

        # 转换为Tensor
        v = torch.tensor(sobel_v_weights[ksize], dtype=torch.float32, device=device)
        h = torch.tensor(h_grad, dtype=torch.float32, device=device)

        # 通过外积生成核矩阵
        sobel_x = torch.outer(v, h)  # 水平核
        sobel_y = sobel_x.T  # 垂直核（转置矩阵）

        # 标准化处理
        # max_val = torch.max(torch.abs(sobel_x))
        sobel_x = (sobel_x).view(1, 1, ksize, ksize)
        sobel_y = (sobel_y).view(1, 1, ksize, ksize)
        sobel_x_list.append(sobel_x)
        sobel_y_list.append(sobel_y)

        # 计算梯度 [b,1,h,w]
        grad_x = F.conv2d(gray_img, sobel_x, padding=kernel_config[scale]['padding'])
        grad_y = F.conv2d(gray_img, sobel_y, padding=kernel_config[scale]['padding'])
        grad_x_list.append(grad_x)
        grad_y_list.append(grad_y)
        # 计算梯度幅值并归一化
        grad_mag = torch.sqrt(grad_x ** 2 + grad_y ** 2 + 1e-8)  # 防止除零
        grad_mag = (grad_mag - grad_mag.min()) / (grad_mag.max() - grad_mag.min() + 1e-8)
        # grad_mag_list.append(grad_mag)
        # 生成边缘掩码 [b,1,h,w]
        edge_mask = (grad_mag > threshold).float()
        edge_mask_list.append(edge_mask)

    # 应用目标区域掩码（取第一个通道处理）
    fused_edge_mask = torch.stack(edge_mask_list).sum(dim=0).clamp(0, 1)

    return fused_edge_mask, grad_x_list[0], grad_y_list[0]

def SCG(image_tensor: torch.Tensor, edge_mask: torch.Tensor, targets_mask: torch.Tensor, grad_x: torch.Tensor, grad_y: torch.Tensor) -> torch.Tensor:
    # 参数校验
    if (image_tensor.shape[2:] != edge_mask.shape[2:]) or (image_tensor.shape[2:] != targets_mask.shape[2:]):
        raise ValueError("图像和边缘掩码、目标区域掩码shape必须一致")

    # 参数约束
    main_strength = 1.00
    angle_factor = 0.2
    decay_factor = 0.1
    # 动态范围调节
    delta = 0.   # 降低图像整体亮度
    image_tensor = torch.clamp(image_tensor-delta, -1, 1)

    edge_mask = edge_mask * targets_mask
    edge_mask = edge_mask.expand_as(image_tensor)  # [b,c,h,w]
    scatter_field = main_strength * edge_mask

    # 方位角依赖
    edge_orientation = torch.atan2(grad_y, grad_x + 1e-6)
    angle_weight = 1.0 + angle_factor * torch.cos(2 * edge_orientation)
    scatter_field *= angle_weight

    # 空间衰减
    _, _, H, W = image_tensor.shape
    pool_size = max(3, int(3 * max(H, W) / 256))
    pool_size = pool_size if pool_size % 2 == 1 else pool_size + 1  # 保证奇数
    dist_map = 1 - F.avg_pool2d(edge_mask, pool_size, stride=1, padding=pool_size // 2)
    scatter_field *= torch.exp(-decay_factor * dist_map * 5)

    enhanced = torch.where(edge_mask.bool(), torch.maximum(image_tensor, scatter_field), image_tensor)
    return torch.clamp(enhanced, -1, 1)

import matplotlib.pyplot as plt
def visualize_tensor(tensor, index=0, denormalize=True):
    """
    可视化指定批次索引的图像
    Args:
        tensor: 输入张量 [B, C, H, W]
        index: 批次索引（默认0）
        denormalize: 是否反归一化（适用于归一化到[-1,1]或[0,1]的情况）
    """
    img = tensor[index].detach().cpu()
    # 反归一化
    if denormalize:
        img = (img * 0.5) + 0.5  # [-1,1] → [0,1]
        # 若原始范围是 [0,1] 则无需操作
    # 调整维度顺序 C×H×W → H×W×C
    img = img.permute(1, 2, 0).numpy()
    # 确保像素值在 [0,1] 范围内
    img = img.clip(0, 1)

    plt.imshow(img)
    plt.axis('off')
    plt.show()

class ResnetBlock(nn.Module):
    """Define a Resnet block"""

    def __init__(self, dim, padding_type, norm_layer, use_dropout, use_bias):
        """Initialize the Resnet block

        A resnet block is a conv block with skip connections
        We construct a conv block with build_conv_block function,
        and implement skip connections in <forward> function.
        Original Resnet paper: https://arxiv.org/pdf/1512.03385.pdf
        """
        super(ResnetBlock, self).__init__()
        self.conv_block = self.build_conv_block(dim, padding_type, norm_layer, use_dropout, use_bias)

    def build_conv_block(self, dim, padding_type, norm_layer, use_dropout, use_bias):
        """Construct a convolutional block.

        Parameters:
            dim (int)           -- the number of channels in the conv layer.
            padding_type (str)  -- the name of padding layer: reflect | replicate | zero
            norm_layer          -- normalization layer
            use_dropout (bool)  -- if use dropout layers.
            use_bias (bool)     -- if the conv layer uses bias or not

        Returns a conv block (with a conv layer, a normalization layer, and a non-linearity layer (ReLU))
        """
        conv_block = []
        p = 0
        if padding_type == 'reflect':
            conv_block += [nn.ReflectionPad2d(1)]
        elif padding_type == 'replicate':
            conv_block += [nn.ReplicationPad2d(1)]
        elif padding_type == 'zero':
            p = 1
        else:
            raise NotImplementedError('padding [%s] is not implemented' % padding_type)

        conv_block += [nn.Conv2d(dim, dim, kernel_size=3, padding=p, bias=use_bias), norm_layer(dim), nn.ReLU(True)]
        if use_dropout:
            conv_block += [nn.Dropout(0.5)]

        p = 0
        if padding_type == 'reflect':
            conv_block += [nn.ReflectionPad2d(1)]
        elif padding_type == 'replicate':
            conv_block += [nn.ReplicationPad2d(1)]
        elif padding_type == 'zero':
            p = 1
        else:
            raise NotImplementedError('padding [%s] is not implemented' % padding_type)
        conv_block += [nn.Conv2d(dim, dim, kernel_size=3, padding=p, bias=use_bias), norm_layer(dim)]

        return nn.Sequential(*conv_block)

    def forward(self, x):
        """Forward function (with skip connections)"""
        out = x + self.conv_block(x)  # add skip connections
        return out


class UnetGenerator(nn.Module):
    """Create a Unet-based generator"""

    def __init__(self, input_nc, output_nc, num_downs, ngf=64, norm_layer=nn.BatchNorm2d, use_dropout=False):
        """Construct a Unet generator
        Parameters:
            input_nc (int)  -- the number of channels in input images
            output_nc (int) -- the number of channels in output images
            num_downs (int) -- the number of downsamplings in UNet. For example, # if |num_downs| == 7,
                                image of size 128x128 will become of size 1x1 # at the bottleneck
            ngf (int)       -- the number of filters in the last conv layer
            norm_layer      -- normalization layer

        We construct the U-Net from the innermost layer to the outermost layer.
        It is a recursive process.
        """
        super(UnetGenerator, self).__init__()
        # construct unet structure
        unet_block = UnetSkipConnectionBlock(ngf * 8, ngf * 8, input_nc=None, submodule=None, norm_layer=norm_layer, innermost=True)  # add the innermost layer
        for i in range(num_downs - 5):          # add intermediate layers with ngf * 8 filters
            unet_block = UnetSkipConnectionBlock(ngf * 8, ngf * 8, input_nc=None, submodule=unet_block, norm_layer=norm_layer, use_dropout=use_dropout)
        # gradually reduce the number of filters from ngf * 8 to ngf
        unet_block = UnetSkipConnectionBlock(ngf * 4, ngf * 8, input_nc=None, submodule=unet_block, norm_layer=norm_layer)
        unet_block = UnetSkipConnectionBlock(ngf * 2, ngf * 4, input_nc=None, submodule=unet_block, norm_layer=norm_layer)
        unet_block = UnetSkipConnectionBlock(ngf, ngf * 2, input_nc=None, submodule=unet_block, norm_layer=norm_layer)
        self.model = UnetSkipConnectionBlock(output_nc, ngf, input_nc=input_nc, submodule=unet_block, outermost=True, norm_layer=norm_layer)  # add the outermost layer

    def forward(self, input):
        """Standard forward"""
        return self.model(input)


class UnetSkipConnectionBlock(nn.Module):
    """Defines the Unet submodule with skip connection.
        X -------------------identity----------------------
        |-- downsampling -- |submodule| -- upsampling --|
    """

    def __init__(self, outer_nc, inner_nc, input_nc=None,
                 submodule=None, outermost=False, innermost=False, norm_layer=nn.BatchNorm2d, use_dropout=False):
        """Construct a Unet submodule with skip connections.

        Parameters:
            outer_nc (int) -- the number of filters in the outer conv layer
            inner_nc (int) -- the number of filters in the inner conv layer
            input_nc (int) -- the number of channels in input images/features
            submodule (UnetSkipConnectionBlock) -- previously defined submodules
            outermost (bool)    -- if this module is the outermost module
            innermost (bool)    -- if this module is the innermost module
            norm_layer          -- normalization layer
            use_dropout (bool)  -- if use dropout layers.
        """
        super(UnetSkipConnectionBlock, self).__init__()
        self.outermost = outermost
        if type(norm_layer) == functools.partial:
            use_bias = norm_layer.func == nn.InstanceNorm2d
        else:
            use_bias = norm_layer == nn.InstanceNorm2d
        if input_nc is None:
            input_nc = outer_nc
        downconv = nn.Conv2d(input_nc, inner_nc, kernel_size=4,
                             stride=2, padding=1, bias=use_bias)
        downrelu = nn.LeakyReLU(0.2, True)
        downnorm = norm_layer(inner_nc)
        uprelu = nn.ReLU(True)
        upnorm = norm_layer(outer_nc)

        if outermost:
            upconv = nn.ConvTranspose2d(inner_nc * 2, outer_nc,
                                        kernel_size=4, stride=2,
                                        padding=1)
            down = [downconv]
            up = [uprelu, upconv, nn.Tanh()]
            model = down + [submodule] + up
        elif innermost:
            upconv = nn.ConvTranspose2d(inner_nc, outer_nc,
                                        kernel_size=4, stride=2,
                                        padding=1, bias=use_bias)
            down = [downrelu, downconv]
            up = [uprelu, upconv, upnorm]
            model = down + up
        else:
            upconv = nn.ConvTranspose2d(inner_nc * 2, outer_nc,
                                        kernel_size=4, stride=2,
                                        padding=1, bias=use_bias)
            down = [downrelu, downconv, downnorm]
            up = [uprelu, upconv, upnorm]

            if use_dropout:
                model = down + [submodule] + up + [nn.Dropout(0.5)]
            else:
                model = down + [submodule] + up

        self.model = nn.Sequential(*model)

    def forward(self, x):
        if self.outermost:
            return self.model(x)
        else:   # add skip connections
            return torch.cat([x, self.model(x)], 1)


class NLayerDiscriminator(nn.Module):
    """Defines a PatchGAN discriminator"""

    def __init__(self, input_nc, ndf=64, n_layers=3, norm_layer=nn.BatchNorm2d):
        """Construct a PatchGAN discriminator

        Parameters:
            input_nc (int)  -- the number of channels in input images
            ndf (int)       -- the number of filters in the last conv layer
            n_layers (int)  -- the number of conv layers in the discriminator
            norm_layer      -- normalization layer
        """
        super(NLayerDiscriminator, self).__init__()
        if type(norm_layer) == functools.partial:  # no need to use bias as BatchNorm2d has affine parameters
            use_bias = norm_layer.func == nn.InstanceNorm2d
        else:
            use_bias = norm_layer == nn.InstanceNorm2d

        kw = 4
        padw = 1
        sequence = [nn.Conv2d(input_nc, ndf, kernel_size=kw, stride=2, padding=padw), nn.LeakyReLU(0.2, True)]
        nf_mult = 1
        nf_mult_prev = 1
        for n in range(1, n_layers):  # gradually increase the number of filters
            nf_mult_prev = nf_mult
            nf_mult = min(2 ** n, 8)
            sequence += [
                nn.Conv2d(ndf * nf_mult_prev, ndf * nf_mult, kernel_size=kw, stride=2, padding=padw, bias=use_bias),
                norm_layer(ndf * nf_mult),
                nn.LeakyReLU(0.2, True)
            ]

        nf_mult_prev = nf_mult
        nf_mult = min(2 ** n_layers, 8)
        sequence += [
            nn.Conv2d(ndf * nf_mult_prev, ndf * nf_mult, kernel_size=kw, stride=1, padding=padw, bias=use_bias),
            norm_layer(ndf * nf_mult),
            nn.LeakyReLU(0.2, True)
        ]

        sequence += [nn.Conv2d(ndf * nf_mult, 1, kernel_size=kw, stride=1, padding=padw)]  # output 1 channel prediction map
        self.model = nn.Sequential(*sequence)

    def forward(self, input):
        """Standard forward."""
        return self.model(input)


class PixelDiscriminator(nn.Module):
    """Defines a 1x1 PatchGAN discriminator (pixelGAN)"""

    def __init__(self, input_nc, ndf=64, norm_layer=nn.BatchNorm2d):
        """Construct a 1x1 PatchGAN discriminator

        Parameters:
            input_nc (int)  -- the number of channels in input images
            ndf (int)       -- the number of filters in the last conv layer
            norm_layer      -- normalization layer
        """
        super(PixelDiscriminator, self).__init__()
        if type(norm_layer) == functools.partial:  # no need to use bias as BatchNorm2d has affine parameters
            use_bias = norm_layer.func == nn.InstanceNorm2d
        else:
            use_bias = norm_layer == nn.InstanceNorm2d

        self.net = [
            nn.Conv2d(input_nc, ndf, kernel_size=1, stride=1, padding=0),
            nn.LeakyReLU(0.2, True),
            nn.Conv2d(ndf, ndf * 2, kernel_size=1, stride=1, padding=0, bias=use_bias),
            norm_layer(ndf * 2),
            nn.LeakyReLU(0.2, True),
            nn.Conv2d(ndf * 2, 1, kernel_size=1, stride=1, padding=0, bias=use_bias)]

        self.net = nn.Sequential(*self.net)

    def forward(self, input):
        """Standard forward."""
        return self.net(input)

def kaiming_init(module, a=0, mode='fan_out', nonlinearity='relu', bias=0, distribution='normal'):
    assert distribution in ['uniform', 'normal']
    if distribution == 'uniform':
        nn.init.kaiming_uniform_(
            module.weight, a=a, mode=mode, nonlinearity=nonlinearity)
    else:
        nn.init.kaiming_normal_(
            module.weight, a=a, mode=mode, nonlinearity=nonlinearity)
    if hasattr(module, 'bias') and module.bias is not None:
        nn.init.constant_(module.bias, bias)

class GFE(nn.Module):
    def __init__(self, inplanes, planes, kernel_size=1, stride=1):
        super(GFE, self).__init__()

        self.inplanes = inplanes
        self.inter_planes = planes // 2
        self.planes = planes
        self.kernel_size = kernel_size
        self.stride = stride
        self.padding = (kernel_size-1)//2

        self.conv_q_right = nn.Conv2d(self.inplanes, 1, kernel_size=1, stride=stride, padding=0, bias=False)
        self.conv_v_right = nn.Conv2d(self.inplanes, self.inter_planes, kernel_size=1, stride=stride, padding=0, bias=False)
        self.conv_up = nn.Conv2d(self.inter_planes, self.planes, kernel_size=1, stride=1, padding=0, bias=False)
        self.softmax_right = nn.Softmax(dim=2)
        self.sigmoid = nn.Sigmoid()

        self.conv_q_left = nn.Conv2d(self.inplanes, self.inter_planes, kernel_size=1, stride=stride, padding=0, bias=False)   #g
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.conv_v_left = nn.Conv2d(self.inplanes, self.inter_planes, kernel_size=1, stride=stride, padding=0, bias=False)   #theta
        self.softmax_left = nn.Softmax(dim=2)

        self.reset_parameters()

    def reset_parameters(self):
        kaiming_init(self.conv_q_right, mode='fan_in')
        kaiming_init(self.conv_v_right, mode='fan_in')
        kaiming_init(self.conv_q_left, mode='fan_in')
        kaiming_init(self.conv_v_left, mode='fan_in')

        self.conv_q_right.inited = True
        self.conv_v_right.inited = True
        self.conv_q_left.inited = True
        self.conv_v_left.inited = True

    def spatial_pool(self, x):
        input_x = self.conv_v_right(x)
        batch, channel, height, width = input_x.size()
        input_x = input_x.view(batch, channel, height * width)
        context_mask = self.conv_q_right(x)
        context_mask = context_mask.view(batch, 1, height * width)
        context_mask = self.softmax_right(context_mask)

        context = torch.matmul(input_x, context_mask.transpose(1,2))
        context = context.unsqueeze(-1)
        context = self.conv_up(context)
        mask_ch = self.sigmoid(context)
        out = x * mask_ch
        return out

    def channel_pool(self, x):
        g_x = self.conv_q_left(x)
        batch, channel, height, width = g_x.size()
        avg_x = self.avg_pool(g_x)
        batch, channel, avg_x_h, avg_x_w = avg_x.size()
        avg_x = avg_x.view(batch, channel, avg_x_h * avg_x_w).permute(0, 2, 1)
        theta_x = self.conv_v_left(x).view(batch, self.inter_planes, height * width)

        context = torch.matmul(avg_x, theta_x)
        context = self.softmax_left(context)
        context = context.view(batch, 1, height, width)
        mask_sp = self.sigmoid(context)
        out = x * mask_sp
        return out

    def forward(self, x):
        context_channel = self.spatial_pool(x)
        context_spatial = self.channel_pool(x)

        out = context_spatial + context_channel
        return out